package practice.three;

public class Gugudan {
	public static void main(String[] args) {
		// 6단
		System.out.println("6단");
		int i = 1;
		while(i < 10) {
			System.out.println(6 * i); 
			i = i + 1;// i++
		}//while end
		
		// 7단
		System.out.println("7단");
		for(int j = 1; j < 10; j++) {
			System.out.println(7 * j);
		}//for end
		
	}
}
